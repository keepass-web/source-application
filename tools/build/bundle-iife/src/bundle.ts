import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import type { BundleConfig } from './types.ts';

/**
 * Strip ES module syntax from a compiled JS file so it can be concatenated
 * with other files in a single classic <script> scope.
 *
 * Rules:
 *   import { ... } from '...'   → removed (names are in scope from prior files)
 *   export { ... } from '...'   → removed (re-exports, not needed)
 *   export { ... }              → removed (names already declared below)
 *   export function/class/const → strip the `export` keyword only
 */
function stripModuleSyntax(src: string): string {
  return src
    .replace(/^import\b.*$/gm, '')
    .replace(/^export\s*\{[^}]*\}\s*from\s*['"][^'"]*['"];?\s*$/gm, '')
    .replace(/^export\s*\{[^}]*\};?\s*$/gm, '')
    .replace(/^export\s+(async\s+function|function|class|const|let|var)\b/gm, '$1');
}

/**
 * Reads the config at `configPath`, concatenates the listed dist files into a
 * single IIFE, hoists the named exports onto `globalThis`, writes the output
 * file, and returns the number of files bundled.
 */
export function bundle(configPath: string): number {
  const base = dirname(resolve(configPath));
  const config: BundleConfig = JSON.parse(readFileSync(configPath, 'utf8')) as BundleConfig;
  const nm = resolve(base, config.packagesDir);

  const parts: string[] = [
    '/* keepass-web library bundle — generated by bundle-iife */',
    '(function() {',
  ];

  for (const file of config.files) {
    const src = readFileSync(join(nm, file), 'utf8');
    parts.push(`\n/* === ${file} === */`);
    parts.push(stripModuleSyntax(src));
  }

  parts.push('\n/* --- public exports --- */');
  for (const name of config.exports) {
    parts.push(`if (typeof ${name} !== 'undefined') globalThis.${name} = ${name};`);
  }
  parts.push('})();');

  const outputPath = join(base, config.output);
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, parts.join('\n'), 'utf8');

  return config.files.length;
}
